        // ── Descrittore di un singolo endpoint ───────────────────────────────────
        internal class EndpointInfo
        {
            /// <summary>Path relativo rispetto alla base (uguale per test e prod)</summary>
            public string PathRelativo { get; }

            /// <summary>SOAPAction da usare nell'header HTTP</summary>
            public string SoapAction { get; }

            /// <summary>Nome dell'operazione SOAP (local part)</summary>
            public string OperazioneWsdl { get; }

            public bool UsaBaseA2f { get; }

            public string? NamespaceSoap { get; }

            /// <summary>Se true, usa SSL con certificato client. Se false, usa Basic Auth.</summary>
            public bool UsaSslClientCert { get; }

            /// <summary>Se true, valida il certificato del server usando la CA (solo prescrittore in produzione).</summary>
            public bool UsaValidazioneCA { get; }

            /// <summary>Prefisso XML namespace da usare nell'envelope (es. "inv", "tns", "aut")</summary>
            public string PrefissoNs { get; }

            /// <summary>Se false, non include xmlns:dat nell'envelope (es. InvioPrescritto usa solo tip:)</summary>
            public bool UsaDatNamespace { get; }

            public EndpointInfo(string pathRelativo, string soapAction, string operazioneWsdl,
                bool usaBaseA2f = false, string? namespaceSoap = null,
                bool usaSslClientCert = false, bool usaValidazioneCA = false,
                string prefissoNs = "tns", bool usaDatNamespace = true)
            {
                PathRelativo      = pathRelativo;
                SoapAction        = soapAction;
                OperazioneWsdl    = operazioneWsdl;
                UsaBaseA2f        = usaBaseA2f;
                NamespaceSoap     = namespaceSoap;
                UsaSslClientCert  = usaSslClientCert;
                UsaValidazioneCA  = usaValidazioneCA;
                PrefissoNs        = prefissoNs;
                UsaDatNamespace   = usaDatNamespace;
            }

            public string GetUrl(ServiceEnvironment ambiente)
            {
                if (UsaBaseA2f)
                    return (ambiente == ServiceEnvironment.Test ? BaseA2fTest : BaseA2fProd) + PathRelativo;
                return (ambiente == ServiceEnvironment.Test ? BaseTest : BaseProd) + PathRelativo;
            }
        }

